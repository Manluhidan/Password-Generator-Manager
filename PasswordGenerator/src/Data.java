import java.util.*;
public class Data {
	
	public String username;
	public String password;

	public Data(String username, String password) {
		
		this.username = username;
		this.password = password;
	}
	
	public String getUsername() {
		
		return username;
	}
	
	public String getPassword() {
		return password;
	}
	
}
